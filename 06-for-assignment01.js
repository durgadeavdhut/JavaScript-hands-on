console.log("print number from 5 to 15");
for (let i = 5; i <= 15; i++) {
   console.log(i);
}

console.log("print number from 50 to 40 in decrement order");
for (let i = 50; i >= 40; i--) {
   console.log(i);
}


console.log("first 15 odd number");
for (let i = 1; i <= 30; i++) {
   if (i % 2 == 1) {
      console.log(i);
   }
}

console.log("first 15 Even number");
for (let i = 0; i <= 20; i++) {
   if (i % 2 == 0) {
      console.log(i);
   }
}

console.log("Table of 5");
for (let i = 1; i <= 10; i++) {
   console.log(5 * i);
}

console.log("Table of 10");
for (let i = 1; i <= 10; i++) {
   console.log(10 * i);
}

console.log("Table of 10 in reverse order");
for (let i = 10; i >= 1; i--) {
   console.log(10 * i);
}